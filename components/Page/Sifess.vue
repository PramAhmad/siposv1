<script setup>
const supabase = useSupabaseClient();
const datas = ref([]);
const coment = ref([]);
const komentar = ref("");
const id_sifest = ref(0);
const loading = ref(false);
const displayedData = ref([]);

let showModal = ref(false);

const getSifest = async () => {
  const { data, error } = await supabase
    .from("sifest")
    .select()
    .order("id", { ascending: false })
    .eq("is_accept", 1)
    .limit(3);

  if (error) {
    console.log(error);
  } else {
    datas.value = data;
    console.log(data);
    displayedData.value = datas.value.slice(0, 3);
  }
};

const getKomentar = async (id) => {
  showModal.value = !showModal.value;
  loading.value = true;
  id_sifest.value = id;
  const { data, error } = await supabase
    .from("komentar")
    .select()
    .eq("id_sifest", id);
  if (error) {
    console.log(error);
  } else {
    coment.value = data;
    loading.value = false;
  }
};

const closeModal = () => {
  showModal.value = !showModal.value;
  // kosongkan coment
  coment.value = [];
};
const addKomentar = async () => {
  const { data, error } = await supabase
    .from("komentar")
    .insert([{ id_sifest: id_sifest.value, msg: komentar.value }]);
  if (error) {
    console.log(error);
  } else {
    coment.value = [];
    komentar.value = "";
    getKomentar(id_sifest.value);
  }
};

onMounted(() => {
  getSifest();
});
</script>
